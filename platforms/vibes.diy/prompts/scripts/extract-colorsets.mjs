#!/usr/bin/env node
// One-shot migration helper for ticket #1853.
//
// For each prompts/pkg/themes/<slug>.md:
//   1. Reads the frontmatter
//   2. Extracts `name`, `colors:` block, optional `colorsDark:` block
//   3. Writes prompts/pkg/themes/colors/<slug>.yaml with that data
//   4. Rewrites the .md to remove the `colors:` and `colorsDark:` blocks
//      from the frontmatter (leaves the markdown body intact)
//
// Run from the prompts/ directory: `node scripts/extract-colorsets.mjs`

import { readFile, writeFile, readdir, mkdir } from "node:fs/promises";
import { join, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const themesDir = join(here, "..", "pkg", "themes");
const colorsDir = join(themesDir, "colors");

function splitFrontmatter(md) {
  const m = md.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (!m) return { frontmatter: "", body: md };
  return { frontmatter: m[1], body: m[2] };
}

function extractName(fm) {
  const m = fm.match(/^name:\s*(.+)$/m);
  return m ? m[1].trim() : "Untitled";
}

// Match a top-level key (column 0) followed by its indented child lines.
// Stops when another column-0 key appears or the frontmatter ends.
function extractBlock(fm, key) {
  const lines = fm.split("\n");
  let start = -1;
  for (let i = 0; i < lines.length; i++) {
    if (lines[i].match(new RegExp(`^${key}:\\s*$`)) || lines[i].match(new RegExp(`^${key}:\\s+`))) {
      start = i;
      break;
    }
  }
  if (start === -1) return { found: false, block: "", body: "", remaining: fm };
  let end = lines.length;
  for (let i = start + 1; i < lines.length; i++) {
    // A new top-level key starts (non-space, non-comment first char).
    if (lines[i].length > 0 && !lines[i].startsWith(" ") && !lines[i].startsWith("\t") && !lines[i].startsWith("#")) {
      end = i;
      break;
    }
  }
  const block = lines.slice(start, end).join("\n");
  const body = lines.slice(start + 1, end).join("\n");
  const remaining = [...lines.slice(0, start), ...lines.slice(end)].join("\n");
  return { found: true, block, body, remaining };
}

function buildYaml(name, colorsBody, colorsDarkBody) {
  const parts = [`name: ${name}`, "colors:", colorsBody.replace(/\n+$/, "")];
  if (colorsDarkBody !== null) {
    parts.push("colorsDark:");
    parts.push(colorsDarkBody.replace(/\n+$/, ""));
  }
  return parts.join("\n") + "\n";
}

// Read all *.yaml files under themes/colors/ and emit a single
// colorsets-bundle.ts so frontend code can import the full palette
// for any slug at build time (browsers can't load .yaml at runtime
// from the prompts pkg). Run independently of the extraction step
// so it picks up hand-edited YAMLs and new colorsets that don't
// correspond to a structural theme.
async function buildBundle() {
  const yamlFiles = (await readdir(colorsDir)).filter((f) => f.endsWith(".yaml"));
  const entries = [];
  for (const file of yamlFiles) {
    const slug = basename(file, ".yaml");
    const raw = await readFile(join(colorsDir, file), "utf8");
    entries.push([slug, parseYamlMinimal(raw)]);
  }
  entries.sort(([a], [b]) => a.localeCompare(b));

  const lines = [
    "// AUTO-GENERATED by scripts/extract-colorsets.mjs — do not edit by hand.",
    "// Run `node scripts/extract-colorsets.mjs` after adding or modifying any",
    "// file in themes/colors/.",
    "",
    'import type { Colorset } from "./colorsets.js";',
    "",
    "export const vibesColorsets: Record<string, Colorset> = {",
  ];
  for (const [slug, cs] of entries) {
    lines.push(`  ${JSON.stringify(slug)}: ${formatColorset(cs)},`);
  }
  lines.push("};", "");
  lines.push("export function getColorsetBySlug(slug: string): Colorset | undefined {");
  lines.push("  return vibesColorsets[slug];");
  lines.push("}", "");
  await writeFile(join(themesDir, "colorsets-bundle.ts"), lines.join("\n"), "utf8");
  console.log(`bundled ${entries.length} colorsets to colorsets-bundle.ts`);
}

// Mirror of CANONICAL_TOKENS in colorsets.ts. Build-time only — the runtime
// path uses the TS exports directly. Keep these two in sync; tests validate
// the bundled shape matches what parseColorsetYaml would produce.
const CANONICAL_TOKENS = [
  "background",
  "surface",
  "primary",
  "secondary",
  "accent",
  "text-primary",
  "text-secondary",
  "text-disabled",
  "border",
  "success",
  "warning",
  "error",
  "neutral",
];
const CANONICAL_SET = new Set(CANONICAL_TOKENS);
const TOKEN_ALIASES = {
  bg: "background",
  "bg-dark": "background",
  "color-background": "background",
  "page-bg": "background",
  card: "surface",
  "card-bg": "surface",
  "card-background": "surface",
  "comp-bg": "surface",
  "bg-card": "surface",
  "bg-surface": "surface",
  "bg-panel": "surface",
  panel: "surface",
  "panel-bg": "surface",
  "comp-accent": "primary",
  "comp-secondary": "secondary",
  "comp-accent-secondary": "secondary",
  fg: "text-primary",
  text: "text-primary",
  ink: "text-primary",
  "comp-text": "text-primary",
  muted: "text-secondary",
  "fg-muted": "text-secondary",
  "fg-dim": "text-secondary",
  "comp-muted": "text-secondary",
  "text-muted": "text-secondary",
  "text-dim": "text-secondary",
  "comp-text-secondary": "text-secondary",
  outline: "border",
  stroke: "border",
  rule: "border",
  separator: "border",
  "comp-border": "border",
  "comp-success": "success",
  danger: "error",
  "comp-danger": "error",
};

function splitCanonical(raw) {
  const colors = {};
  const extras = {};
  for (const [key, value] of Object.entries(raw)) {
    if (CANONICAL_SET.has(key)) colors[key] = value;
  }
  for (const [key, value] of Object.entries(raw)) {
    if (CANONICAL_SET.has(key)) continue;
    const alias = TOKEN_ALIASES[key];
    if (alias && CANONICAL_SET.has(alias) && !colors[alias]) {
      colors[alias] = value;
    } else {
      extras[key] = value;
    }
  }
  return { colors, extras };
}

const CANONICAL_STRUCTURAL = [
  "font-family",
  "font-family-mono",
  "font-size-base",
  "radius",
  "radius-sm",
  "radius-lg",
  "spacing",
  "border-width",
];
const STRUCTURAL_SET = new Set(CANONICAL_STRUCTURAL);

function splitStructural(raw) {
  const structural = {};
  const extras = {};
  for (const [key, value] of Object.entries(raw)) {
    if (STRUCTURAL_SET.has(key)) structural[key] = value;
    else extras[key] = value;
  }
  return { structural, extras };
}

function parseYamlMinimal(raw) {
  const nameMatch = raw.match(/^name:\s*(.+)$/m);
  const name = nameMatch ? nameMatch[1].trim() : "Untitled";

  const rawColors = readMap(raw, "colors");
  const explicitExtras = /^extras:/m.test(raw) ? readMap(raw, "extras") : {};
  const rawDark = /^colorsDark:/m.test(raw) ? readMap(raw, "colorsDark") : undefined;
  const explicitExtrasDark = /^extrasDark:/m.test(raw) ? readMap(raw, "extrasDark") : {};
  const rawStructural = /^structural:/m.test(raw) ? readMap(raw, "structural") : undefined;
  const explicitStructuralExtras = /^structuralExtras:/m.test(raw)
    ? readMap(raw, "structuralExtras")
    : {};

  const light = splitCanonical(rawColors);
  const lightExtras = { ...light.extras, ...explicitExtras };

  const result = { name, colors: light.colors };
  if (Object.keys(lightExtras).length > 0) result.extras = lightExtras;
  if (rawDark) {
    const dark = splitCanonical(rawDark);
    const darkExtras = { ...dark.extras, ...explicitExtrasDark };
    result.colorsDark = dark.colors;
    if (Object.keys(darkExtras).length > 0) result.extrasDark = darkExtras;
  }
  if (rawStructural) {
    const struct = splitStructural(rawStructural);
    const structExtras = { ...struct.extras, ...explicitStructuralExtras };
    if (Object.keys(struct.structural).length > 0) result.structural = struct.structural;
    if (Object.keys(structExtras).length > 0) result.structuralExtras = structExtras;
  } else if (Object.keys(explicitStructuralExtras).length > 0) {
    result.structuralExtras = explicitStructuralExtras;
  }
  return result;
}

function readMap(raw, key) {
  const out = {};
  const re = new RegExp(`^${key}:[ \\t]*\\n((?:[ \\t]+.*\\n?)*)`, "m");
  const block = raw.match(re);
  if (!block) return out;
  for (const line of block[1].split("\n")) {
    if (!line.trim() || line.trim().startsWith("#")) continue;
    const quoted = line.match(/^[ \t]+([\w-]+):\s*"([^"]*)"/);
    if (quoted) {
      out[quoted[1]] = quoted[2];
      continue;
    }
    const unquoted = line.match(/^[ \t]+([\w-]+):\s*([^#\n]*?)\s*(?:#.*)?$/);
    if (unquoted) out[unquoted[1]] = unquoted[2].trim();
  }
  return out;
}

function formatColorset(cs) {
  const parts = [`name: ${JSON.stringify(cs.name)}`];
  parts.push(`colors: ${JSON.stringify(cs.colors)}`);
  if (cs.colorsDark) parts.push(`colorsDark: ${JSON.stringify(cs.colorsDark)}`);
  if (cs.extras) parts.push(`extras: ${JSON.stringify(cs.extras)}`);
  if (cs.extrasDark) parts.push(`extrasDark: ${JSON.stringify(cs.extrasDark)}`);
  if (cs.structural) parts.push(`structural: ${JSON.stringify(cs.structural)}`);
  if (cs.structuralExtras) parts.push(`structuralExtras: ${JSON.stringify(cs.structuralExtras)}`);
  return `{ ${parts.join(", ")} }`;
}

async function main() {
  await mkdir(colorsDir, { recursive: true });
  const files = (await readdir(themesDir)).filter((f) => f.endsWith(".md"));
  for (const file of files) {
    const slug = basename(file, ".md");
    const path = join(themesDir, file);
    const md = await readFile(path, "utf8");
    const { frontmatter, body } = splitFrontmatter(md);
    if (!frontmatter) {
      console.warn(`skip ${slug}: no frontmatter`);
      continue;
    }
    const name = extractName(frontmatter);
    const colors = extractBlock(frontmatter, "colors");
    if (!colors.found) {
      console.warn(`skip ${slug}: no colors block`);
      continue;
    }
    const dark = extractBlock(colors.remaining, "colorsDark");

    const yaml = buildYaml(name, colors.body, dark.found ? dark.body : null);
    await writeFile(join(colorsDir, `${slug}.yaml`), yaml, "utf8");

    // Rewrite the .md without colors/colorsDark in the frontmatter.
    const fmRest = (dark.found ? dark.remaining : colors.remaining).replace(/\n{3,}/g, "\n\n").replace(/^\n+|\n+$/g, "");
    const nextMd = `---\n${fmRest}\n---\n${body.startsWith("\n") ? "" : "\n"}${body}`;
    await writeFile(path, nextMd, "utf8");
    console.log(`extracted ${slug} (dark=${dark.found ? "yes" : "no"})`);
  }
  await buildBundle();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
